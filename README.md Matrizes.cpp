# Matrizes
#include <stdio.h>

int main() {
    int matriz[3][3];
    int i, j;

    // Entrada dos valores da matriz 3x3
    printf("Digite os valores da matriz 3x3:\n");
    for(i = 0; i < 3; i++) {
        for(j = 0; j < 3; j++) {
            printf("Elemento [%d][%d]: ", i, j);
            scanf("%d", &matriz[i][j]);
        }
    }

    // Imprimindo a diagonal principal de trÃ¡s para frente
    printf("Diagonal principal de tras para frente: \n");
    for(i = 2; i >= 0; i--) {
        printf("%d ", matriz[i][i]);
    }
    printf("\n");

    return 0;
}
